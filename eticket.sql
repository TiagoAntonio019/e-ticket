    SET no_pay= (SELECT sval FROM settings WHERE skey = 'no-pay');
    
    set closing_time = (select closing_date from ticket where id=ticket_id);
    if(closing_time is null) then
        SET entry_time_min = timestampdiff(minute, entry_time, now());
    else
        SET entry_time_min = timestampdiff(minute, entry_time, closing_time);
    end if;
    SET entry_time_hour = entry_time_min / 60;

    #Grace period
    if(entry_time_min > no_pay) then
        set cost = cost_p_hour * entry_time_hour;
    end if;
    
